window.ISM_LOCALE = 'en';

window.t = function(key) {
  const locale = window.ISM_LOCALES[window.ISM_LOCALE] || window.ISM_LOCALES.en;
  return key.split('.').reduce((obj, k) => (obj && obj[k] !== undefined ? obj[k] : null), locale) ?? key;
};

window.applyLocale = function(locale) {
  if (!window.ISM_LOCALES[locale]) locale = 'en';
  window.ISM_LOCALE = locale;
  localStorage.setItem('ism-lang', locale);

  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const value = window.t(key);
    if (value && value !== key) {
      if (el.hasAttribute('data-i18n-html')) {
        el.innerHTML = value;
      } else {
        el.textContent = value;
      }
    }
  });

  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.getAttribute('data-i18n-placeholder');
    const value = window.t(key);
    if (value && value !== key) el.placeholder = value;
  });

  const langSelect = document.getElementById('language-select');
  if (langSelect) langSelect.value = locale;
};

window.initLocale = function() {
  const saved = localStorage.getItem('ism-lang');
  const locale = (saved && window.ISM_LOCALES[saved]) ? saved : 'en';
  window.applyLocale(locale);
};
